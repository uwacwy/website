<p>On Tuesday, June 12th, Mizu Sushi Restaurant hosted a fundraising dinner for the United Way of Albany County. The premise of the fundraiser was simple.  The restaurant agreed to donate back to the United Way 20% of all proceeds from their business that evening including takeout items and all drinks.  The only thing the owners of the restaurant, Lillian and Travis Luu, asked of the United Way is that they promote the event.<p>
<p>Paul Heimer, the Executive Director of the United Way of Albany County, said, "We are thrilled with the idea of teaming up with local businesses like Mizu Sushi to help them generate some more business while finding a creative and innovative way to benefit the community.  And because Mizu Sushi is such a great place to eat, and everyone needs dinner, it is a win-win for everyone involved."</p>
<p>The event generated $515 for the United Way of Albany County.  Additionally, the Luu's decided to donate an additional $485 to make a total contribution of $1,000.  Lillian Luu said, "We are strong believers in the Laramie community and we wanted to do something to make a difference.  We hope this will become an annual event."</p>
<p>Funds for the event will be applied to the 2012 United Way Campaign which will officially start in the fall.  The United Way of Albany County currently assists 14 member agencies through its annual campaign.  They raised a record amount of money last year and just completed the process of determining how much money each local agency will receive for the coming fiscal year.  This too, is a record amount.</p>
